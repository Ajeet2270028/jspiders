package jspider;
import java.util.*;

public class NTimeName {
public static void main(String[] args) {
	Scanner sc=new Scanner(System.in);
	System.out.println("Enter a value");
	int n=sc.nextInt();
	System.out.println("Enter name:");
	String name=sc.next();
	
	while(n>0) {
	   System.out.println(name);
	   n--;
		
	}
	}
}
